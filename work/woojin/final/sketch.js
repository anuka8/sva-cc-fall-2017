var mappa = new Mappa('Google', key);
var myMap;
var t;
var canvas;

function setup(){
  canvas = createCanvas(800, 700);
  myMap = mappa.tileMap(options);
  myMap.overlay(canvas);
  noFill();
  noStroke();
  t = 0;
}

function draw(){
  clear();
  colorMode (HSB);

  var x = width * noise(t);
  var y = height * noise(t+5);
  var r = 255 * noise(t+10);
  var g = 255 * noise(t+15);
  var b = 255 * noise(t+20);

   noStroke();
   fill(r, g, b);
   ellipse(x, y, 10, 10);
   t = t + 0.01;
}
